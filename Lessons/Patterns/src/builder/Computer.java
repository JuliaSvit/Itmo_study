package builder;

public class Computer {

    private String display = null;
    private String systemBlock = null;
    private String manipulators = null;

    public String getDisplay() {
        return display;
    }

    public void setDisplay(String display) {
        this.display = display;
    }

    public String getSystemBlock() {
        return systemBlock;
    }

    public void setSystemBlock(String systemBlock) {
        this.systemBlock = systemBlock;
    }

    public String getManipulstors() {
        return manipulators;
    }

    public void setManipulators(String manipulstors) {
        this.manipulators = manipulstors;
    }
}
